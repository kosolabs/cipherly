<h1 class="h1">Cipherly</h1>
<p class="mt-4 text-lg">
  Welcome to Cipherly, a simple password based encryption and decryption tool.
</p>
<p class="mt-4 text-lg">
  Cipherly uses the browser's built-in SubtleCrypto library to provide
  encryption and decryption of text using a password. The password is never sent
  to the server, and the encryption and decryption is done entirely in the
  browser.
</p>
<p class="mt-4 text-lg">
  This tool is a work in progress. Follow along on our
  <a class="text-cyan-500" href="https://www.youtube.com/@FriendlyTL">
    YouTube channel!
  </a>
</p>
